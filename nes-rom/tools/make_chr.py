def tile_for_char(char: str) -> bytes:
    glyph = GLYPHS.get(char, GLYPHS[" "])
    plane0 = []
    for row in glyph:
        value = 0
        for col, bit in enumerate(row):
            if bit == "1":
                value |= 1 << (7 - col)
        plane0.append(value)
    plane0.append(0)
    return bytes(plane0 + [0] * 8)


def main() -> None:
    out = Path(sys.argv[1])
    data = bytearray()
    for code in range(256):
        data += tile_for_char(chr(code))
    data.extend(b"\0" * (8192 - len(data)))
    out.write_bytes(data)


if __name__ == "__main__":
    main()
